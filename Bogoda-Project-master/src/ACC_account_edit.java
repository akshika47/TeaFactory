    private void account_nameFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_account_nameFocusLost
        account_code_label.setText(dbm.checknReturnData("account_names","account_name",account_name.getText(),"account_id"));
        acnt_id=Integer.parseInt(dbm.checknReturnData("account_names","account_name",account_name.getText(),"account_id"));
    }//GEN-LAST:event_account_nameFocusLost

    private void account_codeFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_account_codeFocusLost
       account_name_label.setText(dbm.checknReturnData("account_names","account_id", Integer.parseInt(account_code.getText()),"account_name"));
       acnt_id= Integer.parseInt(account_code.getText());
    }//GEN-LAST:event_account_codeFocusLost

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        dbm.updateDatabase("account_names","account_id",acnt_id,"account_class",new_account_class.getSelectedItem().toString());
        dbm.updateDatabase("account_names","account_id",acnt_id,"opening_balance",Double.parseDouble(new_opening_balance.getText()));
        dbm.updateDatabase("account_names","account_id",acnt_id,"current_balance",Double.parseDouble(new_current_balance.getText()));
    }//GEN-LAST:event_jButton2ActionPerformed

    private void search_byActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_search_byActionPerformed
        if("Name".equals(search_by.getSelectedItem().toString())){
            jPanel1.setVisible(true);
            jPanel2.setVisible(false);
        }
        if("Account Code".equals(search_by.getSelectedItem().toString())){
            jPanel1.setVisible(false);
            jPanel2.setVisible(true);
        }
        
    }//GEN-LAST:event_search_byActionPerformed

